/* ─── Activity hook (session-scoped, real events only) ──────────────────── */

type ActivityItem = {
  id: string;
  Icon: React.ElementType;
  color: string;
  text: string;
  at: number;
};

function useSessionActivity(): ActivityItem[] {
  const [items, setItems] = useState<ActivityItem[]>([]);
  const counterRef = useRef(0);

  useEffect(() => {
    const push = (item: Omit<ActivityItem, "id">) =>
      setItems((prev) =>
        [{ ...item, id: `a_${++counterRef.current}` }, ...prev].slice(0, 30),
      );

    const unsubs = [
      eventBus.on("blueprint:generated", () =>
        push({ Icon: Sparkles, color: "var(--accent-iris)", text: "Blueprint generated by miDirector", at: Date.now() }),
      ),
      eventBus.on("project:created", (e) =>
        push({ Icon: FolderKanban, color: "var(--accent-mint)", text: `Project created`, at: Date.now() }),
      ),
      eventBus.on("project:updated", () =>
        push({ Icon: FolderKanban, color: "var(--accent-mint)", text: "Project updated", at: Date.now() }),
      ),
      eventBus.on("asset:added", (e) =>
        push({ Icon: Upload, color: "var(--accent-mint)", text: `Asset imported (${e.kind})`, at: Date.now() }),
      ),
      eventBus.on("asset:removed", () =>
        push({ Icon: Upload, color: "var(--accent-ember)", text: "Asset removed", at: Date.now() }),
      ),
      eventBus.on("story:created", () =>
        push({ Icon: Clapperboard, color: "var(--accent-gold)", text: "New series created", at: Date.now() }),
      ),
      eventBus.on("ai:generation:started", (e) =>
        push({ Icon: Sparkles, color: "var(--accent-iris)", text: `AI generation started — ${e.agent}`, at: Date.now() }),
      ),
      eventBus.on("ai:generation:completed", (e) =>
        push({ Icon: Sparkles, color: "var(--accent-mint)", text: `AI generation completed — ${e.agent}`, at: Date.now() }),
      ),
      eventBus.on("ai:generation:failed", (e) =>
        push({ Icon: Activity, color: "var(--accent-ember)", text: `Generation failed — ${e.agent}: ${e.error.slice(0, 60)}`, at: Date.now() }),
      ),
      eventBus.on("settings:provider:updated", (e) =>
        push({ Icon: HardDrive, color: "var(--accent-gold)", text: `AI provider ${e.action} — ${e.provider}`, at: Date.now() }),
      ),
    ];

    return () => unsubs.forEach((u) => u());
  }, []);

  return items;
}

/* ─── Helpers ───────────────────────────────────────────────────────────── */

function fmtRelative(ms: number): string {
  const diff = (Date.now() - ms) / 1000;
  if (diff < 60) return "just now";
  if (diff < 3600) return `${Math.floor(diff / 60)}m ago`;
  if (diff < 86400) return `${Math.floor(diff / 3600)}h ago`;
  return `${Math.floor(diff / 86400)}d ago`;
}

/* ─── Page ───────────────────────────────────────────────────────────────── */

function DashboardPage() {
  const projects = useProjectStore((s) => s.projects);
  const conversations = useBlueprintStore((s) => s.conversations);
  const assets = useAssetStore((s) => s.assets);
  const series = useStoryStore((s) => s.series);
  const activity = useSessionActivity();

  const activeProjects = projects.filter((p) => p.status !== "archived").length;
  const blueprintCount = conversations.filter((c) => c.blueprint).length;
  const assetCount = assets.length;
  const seriesCount = series.length;

  return (
    <div className="mx-auto flex w-full max-w-[1480px] flex-col gap-5">
      {/* Stat row */}
      <div className="grid grid-cols-2 gap-3.5 lg:grid-cols-4">
        <StatCard
          accent="iris"
          label="Projects"
          value={String(activeProjects)}
          sub={activeProjects === 1 ? "1 active" : `${activeProjects} active`}
          icon={<FolderKanban className="h-8 w-8" />}
        />
        <StatCard
          accent="mint"
          label="Blueprints"
          value={String(blueprintCount)}
          sub={`${conversations.length} interview${conversations.length !== 1 ? "s" : ""}`}
          icon={<Sparkles className="h-8 w-8" />}
        />
        <StatCard
          accent="gold"
          label="Assets"
          value={String(assetCount)}
          sub={assetCount === 0 ? "nothing imported yet" : `${assetCount} file${assetCount !== 1 ? "s" : ""}`}
          icon={<Upload className="h-8 w-8" />}
        />
        <StatCard
          accent="ember"
          label="Series"
          value={String(seriesCount)}
          sub={seriesCount === 0 ? "none yet" : `${seriesCount} story thread${seriesCount !== 1 ? "s" : ""}`}
          icon={<Film className="h-8 w-8" />}
        />
      </div>

      {/* miDirector hero panel */}
      <DirectorPanel conversationCount={conversations.length} />

      {/* Main grid */}
      <div className="grid grid-cols-1 gap-3.5 lg:grid-cols-[2fr_1fr]">
        <RecentProjectsCard projects={projects} />
        <ActivityFeedCard items={activity} />
      </div>

      <div className="grid grid-cols-1 gap-3.5 lg:grid-cols-[1fr_2fr]">
        <QuickActionsCard />
        <PlatformStatusCard />
      </div>
    </div>
  );
}

/* ─── Stat Card ─────────────────────────────────────────────────────────── */

function StatCard({
  accent,
  label,
  value,
  sub,
  icon,
}: {
  accent: "iris" | "mint" | "ember" | "gold";
  label: string;
  value: string;
  sub: string;
  icon: React.ReactNode;
}) {
  const colorVar =
    accent === "iris"
      ? "var(--accent-iris)"
      : accent === "mint"
        ? "var(--accent-mint)"
        : accent === "ember"
          ? "var(--accent-ember)"
          : "var(--accent-gold)";
  return (
    <div className="hk-card hk-card-hover relative overflow-hidden px-[18px] py-4">
      <div
        className="absolute left-0 right-0 top-0 h-[2px]"
        style={{ background: `linear-gradient(90deg, ${colorVar}, transparent)` }}
      />
      <div className="mb-2 text-[11.5px] font-medium uppercase tracking-[0.3px] text-text-secondary">
        {label}
      </div>
      <div className="hk-text-display mb-1.5 text-[28px] font-bold leading-none text-text-primary">
        {value}
      </div>
      <div className="text-[11.5px] font-medium text-text-secondary">{sub}</div>
      <div className="absolute bottom-3.5 right-4 opacity-10" style={{ color: colorVar }}>
        {icon}
      </div>
    </div>
  );
}

