/* ─── Recent Projects ───────────────────────────────────────────────────── */

import type { Project } from "@/store/project-store";

const STATUS_LABEL: Record<string, string> = {
  draft: "Draft",
  active: "Active",
  published: "Published",
  archived: "Archived",
};

const STATUS_COLOR: Record<string, string> = {
  draft: "text-text-dim",
  active: "text-mint",
  published: "text-iris",
  archived: "text-text-dim",
};

function RecentProjectsCard({ projects }: { projects: Project[] }) {
  const visible = [...projects]
    .filter((p) => p.status !== "archived")
    .sort((a, b) => b.updatedAt - a.updatedAt)
    .slice(0, 5);

  return (
    <div className="hk-card">
      <CardHeader title="Recent Projects">
        <Link
          to="/projects"
          className="text-[11.5px] text-text-secondary hover:text-iris transition-colors"
        >
          View all
        </Link>
      </CardHeader>

      {visible.length === 0 ? (
        <div className="flex flex-col items-center gap-3 px-[18px] py-10 text-center">
          <FolderKanban className="h-8 w-8 text-text-dim opacity-40" />
          <div className="text-[13px] text-text-secondary">No projects yet</div>
          <Link
            to="/projects"
            className="inline-flex h-8 items-center gap-1.5 rounded-md border border-iris/40 bg-iris/10 px-3 text-[12px] font-medium text-iris hover:bg-iris/20"
          >
            <Plus className="h-3 w-3" />
            Create your first project
          </Link>
        </div>
      ) : (
        <div className="divide-y divide-white/[0.06]">
          {visible.map((p) => (
            <div key={p.id} className="flex items-center gap-3 px-[18px] py-3.5">
              <div className="flex h-9 w-9 shrink-0 items-center justify-center rounded-md bg-elevated text-text-secondary">
                <FolderKanban className="h-4 w-4" />
              </div>
              <div className="min-w-0 flex-1">
                <div className="truncate text-[13.5px] font-medium text-text-primary">
                  {p.name}
                </div>
                <div className="mt-0.5 text-[11px] text-text-dim">
                  {fmtRelative(p.updatedAt)}
                </div>
              </div>
              <span className={`text-[11.5px] font-medium ${STATUS_COLOR[p.status] ?? "text-text-dim"}`}>
                {STATUS_LABEL[p.status] ?? p.status}
              </span>
            </div>
          ))}
        </div>
      )}
    </div>
  );
}

